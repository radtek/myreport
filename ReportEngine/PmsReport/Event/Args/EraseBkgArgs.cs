using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Windows.Forms;

namespace PMS.Libraries.ToolControls.Report.Event
{
    public class EraseBkgArgs:EventArgs
    {

        protected bool _hasFinished = false;
        public bool HasFinished
        {
            get
            {
                return _hasFinished;
            }
            set
            {
                _hasFinished = value;
            }
        }
        public EraseBkgArgs()
            : base()
        {
            _hasFinished = false;
        }   

    }
}
